# Basic collections
# 1. List --> mutable sequence of values
# 2. Tuple --> immutable sequence of values
# 3. Set --> mutable collection of unique values
# 4. Dictionary --> mutable collection of key-value pairs

# Advanced collections
# 1. Named Tuple --> immutable sequence of values with names for each value, Tuple with named fields
# 2. Default Dict --> mutable mapping of keys to values, Dictionary with default values dictionary with special properties
# 3. Ordered Dict --> mutable mapping of keys to values
# 4. Counter --> mutable - count distinct values
# 5. Deque --> mutable - double-ended queue or double-ended list object.
# 6. Chain Map --> mutable
# 7. User Dict --> mutable
# 8. User List --> mutable
# 9. User String --> mutable
# 10. Heap Queue --> mutable
# 11. Array --> mutable
# 12. Bytes --> immutable
# 13. Byte Array --> mutable
# 14. Range --> immutable
# 15. Memory View --> immutable

